                <div class="kompressiya__paragraph">
                    <h4 class="kompressiya__paragraph--title">Панорамирование</h4>
                    Панорама в восприятии считывается мгновенно! Ее понимают и музыканты, и звукорежиссеры, и люди, никак не связанные с музыкой. Любой человек заметит в вашем миксе сразу:
                </div>

                <div class="kompressiya__paragraph">
                    <ul class="kompressiya__list">
                        <li class="kompressiya__item">Если что-то слишком громко играет или наоборот, если из лидирующих инструментов что-то пропало (к вопросу о балансе);</li>
                        <li class="kompressiya__item"> Если есть перекос микса в целом или партий-лидеров влево/вправо.</li>
                    </ul>
                </div>
                <div class="kompressiya__paragraph">
                    При перемещении объектов в жизни мы быстро замечаем отклонение от нашей центральной оси. Но, чем дальше находится объект, тем сложнее фиксировать точно его отклонение. Вспомните: самолет, летящий далеко, не четок по панораме, и как трудно понять по звуку, куда он смещается. То есть в миксе далекий объект не ясен по панораме. А значит, довольно центрирован, не может быть в крайних положениях L/R.
                </div>
                <div class="kompressiya__paragraph">
                    Близкие объекты слышно очень ярко. Особенно на 100% L/R. Подумайте, что вы по жизни слышите в этом положении? С какой громкостью? Как долго? Каков ваш слуховой опыт в этом отношении и опыт других людей? Поэтому с единичными объектами, особенно с резким звучанием, в крайних положениях L/R стоит быть осторожным.
                </div>

                <div class="kompressiya__paragraph">
                    Панорама бок о бок идет со стереобазой. Если у вас в миксе все инструменты раскиданы налево и направо в стерео, то разместить что-то в такой ситуации сложно, помимо моно по центру. Если моно-дорожка занимает только одно место, например, R45, то стерео - сразу два, например L100 R100. Чем больше стереозвуков у вас в миксе, тем сложнее их туда «впихнуть».
                </div>

                <div class="kompressiya__paragraph">
                    И помните! хорошо читаются контрасты: в миксе, где основу держат звуки в моно (не значит, что по центру), стерео-дорога будет читаться особенно широко и красиво. В миксе же, где основа - стереопара гитар, синтов или еще чего-то, классно выделится то, что в моно.
                </div>

                <div class="kompressiya__paragraph">
                    <h4 class="kompressiya__paragraph--title">Разница моно и стерео</h4>
                    Если вы создадите моно-дорожку, потом сдублируете и разведете их направо и налево — это будет стерео? Большинство людей отвечает - да! Многие считают, что моно — это один канал, а стерео это два канала L и R. Но это не совсем так, если не сказать - совсем не так. 
                </div>

                <div class="kompressiya__paragraph">
                    <ul class="kompressiya__list">
                        <li class="kompressiya__item">Моно-дорожка — это один сигнал.</li>
                        <li class="kompressiya__item">Стерео-дорожка — это два сигнала с РАЗНОЙ информацией.</li>
                    </ul>
                </div>

                <div class="kompressiya__paragraph">
                    То есть дублированная дорожка, хоть одна, хоть двадцать одна, даже при разведении налево и направо не даст вам стерео!
                </div>

                <div class="kompressiya__paragraph">
                    Пример: Зачем пишут вокальные дабл-треки? Пишется лидирующий вокал, потом вокалист поет еще дважды то же самое. Несмотря на то, что он пел одно и то же, что даже визуально волны похожи друг на друга, это не одни и те же волны. Человек не может спеть или сыграть идентично дважды, потому что он не робот. Где-то он вздохнет иначе, где-то даст немного другую окраску голоса, где-то повернется к микрофону иначе. Поэтому дублировать дорожку с вокалом или спеть несколько раз - ни разу не одно и то же. В итоге: у вас лидирующий по центру и два дабла, один слева, второй справа. В этом случае, вы можете получить широкое сочное стерео.
                </div>


                <div class="kompressiya__paragraph">
                    Вы спросите: а если скопировать дорожку и ее видоизменить? Можно попробовать, например, сдвинуть дорожку по времени или обработать ее как-то особенно эквалайзером, или немного расстроить ноты и т.п. Все это возможно, даст вам улучшенную стерео-картину, но никогда по красочности не сравнится с настоящим стерео.
                </div>

                <div class="kompressiya__paragraph">
                    Если у вас нет стерео-файла, не стоит заниматься изобретением велосипеда и сдвигать дорожки, расстраивать ноты и все вышеперечисленное. Лучше воспользуйтесь готовыми эффектами: chorus, double, pitch shifter и т.д. Они ставятся в send, и при подмешивании дают стерео-иллюзию, добавляя звуки с задержкой и другими изменениями.
                </div>

                <div class="kompressiya__paragraph">
                    Не используйте стерео-расширители на сведении! Да, это больно, но стоит принять - ВЫ НИКОГДА НЕ СДЕЛАЕТЕ ИЗ МОНО сигнала СТЕРЕО! Стерео-расширители (стерео-имеджеры) поставленные в инсерт на моно-канал не сделают его шире, но могут внести деструктивные изменения в звук
